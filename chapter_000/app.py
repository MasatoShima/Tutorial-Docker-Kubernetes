# **************************************************
# ----- Import Library
# **************************************************
import responder


# **************************************************
# ----- Process Main
# **************************************************
api = responder.API()


# **************************************************
# ----- Method
# **************************************************
@api.route("/")
def hello_world(req, res):
	content = "Hello World !\n"

	res.text = content

	return


# **************************************************
# ----- Start
# **************************************************
api.run(address="0.0.0.0", port=8000)


